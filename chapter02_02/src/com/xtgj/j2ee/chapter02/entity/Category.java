package com.xtgj.j2ee.chapter02.entity;
public class Category {
	private int id; // 音乐类别编号
	private String name;// 音乐类别名称
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
   
}

